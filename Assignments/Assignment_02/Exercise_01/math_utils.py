# This is module having methods to calculate area

def area_of_circle(radius):
    print(f"Area of circle is = {3.142*(radius**2)}")

def area_of_square(side):
    print(f"Area of square is = {side**2}")

def area_of_rectangle(width, breadth):
    print(f"Area of rectangle = {width*breadth}")

def area_of_triangle(base, hight):
    print(f"Area of triangle = {(1/2)*base*hight}")

